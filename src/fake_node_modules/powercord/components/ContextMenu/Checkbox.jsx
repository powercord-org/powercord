const { React, getModule } = require('powercord/webpack');

const ItemGroup = require('./ItemGroup');

let classes = null;
setImmediate(async () => {
  classes = {
    itemCheckbox: (await getModule([ 'itemToggle', 'checkbox' ])).checkbox,
    ...await getModule([ 'itemToggle', 'checkbox' ]),
    ...await getModule([ 'checkboxElement' ])
  };
});

module.exports = class CheckboxItem extends React.PureComponent {
  constructor (props) {
    super(props);

    this.state = {
      active: props.defaultState
    };
  }

  onClick () {
    const active = !this.state.active;

    this.props.onToggle(active);
    this.setState({ active });
  }

  render () {
    const checkbox = (
      <div
        className={[ classes.item, classes.itemToggle, classes.clickable ].filter(Boolean).join(' ')}
        onClick={() => this.onClick()}
        role='button'
      >
        <div className={classes.label}>
          {this.props.name}
        </div>

        <div className={[ classes.checkbox, classes.itemCheckbox ].filter(Boolean).join(' ')} role='button'>
          <div className={classes.checkboxInner}>
            <input className={classes.checkboxElement} type='checkbox' checked={this.state.active} />
            <span />
          </div>
        </div>
      </div>
    );

    if (this.props.seperate) {
      return React.createElement(ItemGroup, {
        children: [ checkbox ]
      });
    }

    return checkbox;
  }
};
