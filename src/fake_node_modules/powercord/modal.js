const { React, modal } = require('powercord/webpack');

module.exports = {
  open: (Component) => {
    modal.push(class PowercordModal extends React.Component {
      render () {
        return React.createElement(Component);
      }
    });
  },

  close: () => {
    modal.pop();
  },

  closeAll: () => {
    modal.popAll();
  }
};
